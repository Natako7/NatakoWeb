// Operadores relacionales
console.log(2 == "2");   // Igual (Compara valores)             true
console.log(2 === "2");  // Igual (Compara valores y tipos)     false

console.log(2 != "2");    // Desigual (Compara valores)         false
console.log(2 !== "2");   // Desigual (Compara valores y tipos) true
console.log("--------------------------")

console.log(2 > 5) // Mayor que                 false
console.log(2 >= 5) // Mayor o igual que        false
console.log(2 < 5) // Menor que                 true
console.log(2 <= 5) // Menor o igual que        true
console.log("--------------------------")

